export const MuiTabs = () => {

    const [value, setValue] = useState('1')
    const [searchTerm, setSearchTerm] = useState('')
    const [currentPage, setCurrentPage] = useState(1)
    const [itemsPerPage, setItemsPerPage] = useState(5)

    const maxPage = Math.ceil(data.length / itemsPerPage)
    const theme = useTheme()
    const isMobile = useMediaQuery(theme.breakpoints.down('sm'))
    const filteredData = data.filter(item => String(item.id).includes(searchTerm))
    const startIndex = (currentPage - 1) * itemsPerPage
    const endIndex = (startIndex + itemsPerPage)
    const currentData = filteredData.slice(startIndex, endIndex)

    const handleChange = (event: React.SyntheticEvent, newValue: string) => {
        setValue(newValue)
    }

    const handleChangePage = (_event: React.ChangeEvent<unknown>, value: number) => {
        setCurrentPage(value)
    }

    const handleChangeItemsPerPage = (event: any) => {
        setItemsPerPage(Number(event.target.value))
        setCurrentPage(1)
    }


    return (
        <Box>
            <TabContext value={value}>
                <Box sx={{ borderBottom: 1, borderColor: 'divider' }}>
                    <TabList onChange={handleChange}>
                        <Tab label="Tab 1" value='1' />
                        <Tab label="Tab 2" value='2' />
                        <Tab label="Tab 3" value='3' />
                    </TabList>
                </Box>
                <TabPanel value='1'>
                    <Grid container alignItems="center" spacing={3}>
                        <Grid item xs={6} alignItems='flex-start'>
                            <Typography>Network Builds</Typography>
                        </Grid>
                        <Grid item xs={6} alignItems='flex-end' style={{ textAlign: 'right' }}>
                            <TextField
                                label="Search by ID"
                                value={searchTerm}
                                onChange={e => setSearchTerm(e.target.value)}
                                margin='normal'
                                variant='outlined'
                            />
                        </Grid>
                    </Grid>
                    {isMobile ? (
                        currentData.map(entry => (
                            <Card key={entry.id} sx={{ marginBottom: 2 }}>
                                <CardContent>
                                    <Typography variant='h6' component='div'>
                                        ID: {entry.id}
                                    </Typography>
                                    <Typography color='textSecondary'>
                                        Build Data/Time: {entry['build data/time']}
                                    </Typography>
                                    <Typography>
                                        Number of Devices: {entry['num of devices']}
                                    </Typography>
                                    <Typography>
                                        Build Duration: {entry['build duration']}
                                    </Typography>
                                    <Typography color={entry.status ? "success.main" : "error.main"}>
                                        Status: {entry.status ? "Up" : "Down"}
                                    </Typography>
                                </CardContent>
                            </Card>
                        ))
                    ) : (
                        <Table>
                            <TableHead>
                                <TableRow>
                                    <TableCell>ID</TableCell>
                                    <TableCell>Build Date/Time</TableCell>
                                    <TableCell>Num of Devices</TableCell>
                                    <TableCell>Build Duration</TableCell>
                                    <TableCell>Status</TableCell>
                                </TableRow>
                            </TableHead>
                            <TableBody>
                                {currentData.map(item => (
                                    <TableRow key={item.id}>
                                        <TableCell>{item.id}</TableCell>
                                        <TableCell>{item['build data/time']}</TableCell>
                                        <TableCell>{item['num of devices']}</TableCell>
                                        <TableCell>{item['build duration']}</TableCell>
                                        <TableCell>{item.status ? "Up" : "Down"}</TableCell>
                                    </TableRow>
                                ))}
                            </TableBody>
                        </Table>
                    )}
                    <Box mt={3}>
                        <Grid container alignItems="center" spacing={3}>
                            <Grid item xs={4} alignItems='flex-start'>
                                <Select value={itemsPerPage} onChange={handleChangeItemsPerPage} size='small'>
                                    <MenuItem value={5}>5</MenuItem>
                                    <MenuItem value={10}>10</MenuItem>
                                    <MenuItem value={20}>20</MenuItem>
                                </Select>
                            </Grid>
                            <Grid item xs={8} alignItems='flex-end' style={{ textAlign: 'right' }}>
                                <Pagination count={maxPage} page={currentPage} onChange={handleChangePage} size='large' />
                            </Grid>
                        </Grid>
                    </Box>
                </TabPanel>
                <TabPanel value='2'>Panel 2</TabPanel>
                <TabPanel value='3'>Panel 3</TabPanel>

            </TabContext>
        </Box>
    )
}